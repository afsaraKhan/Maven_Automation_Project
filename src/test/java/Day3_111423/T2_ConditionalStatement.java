package Day3_111423;

public class T2_ConditionalStatement {
    public static void main(String[] args) {
        //declare some integers
        int a = 26, b = 7;

        //check which number is greater
        if (a > b) {
            System.out.println(a + " is greater than " + b);
        } else {
            System.out.println(b + " is greater than " + a);
        } // end of if-else

    } // end of main
} // end of class
